// GET /api/patient/prescriptions/:id/pdf — download prescription PDF
router.get('/prescriptions/:id/pdf', verifyToken, async (req, res) => {
  try {
    const { rows } = await pool.query(
      `SELECT p.*, u.full_name AS doctor_name
       FROM prescriptions p
       JOIN users u ON u.id = p.doctor_id
       WHERE p.id = $1 AND p.patient_id = $2`,
      [req.params.id, req.user.userId]
    );
    if (!rows.length) {
      return res.status(404).json({ error: 'Prescription not found' });
    }
    const rx = rows[0];
    const meds = typeof rx.medications === 'string' ? JSON.parse(rx.medications) : rx.medications;

    const PDFDocument = require('pdfkit');
    const doc = new PDFDocument({ size: 'A4', margin: 50 });
    res.setHeader('Content-Type', 'application/pdf');
    res.setHeader('Content-Disposition', `attachment; filename="prescription-${rx.id}.pdf"`);
    doc.pipe(res);

    // Header
    doc.fontSize(20).fillColor('#0d9488').text('MedAssist AI', { align: 'center' });
    doc.fontSize(12).fillColor('#666').text('Digital Prescription', { align: 'center' });
    doc.moveDown();
    doc.moveTo(50, doc.y).lineTo(545, doc.y).strokeColor('#e2e8f0').stroke();
    doc.moveDown();

    // Doctor & date
    doc.fontSize(11).fillColor('#334155');
    doc.text(`Prescribing Doctor: Dr. ${rx.doctor_name}`);
    doc.text(`Date: ${new Date(rx.issued_at).toLocaleDateString('en-US', { year: 'numeric', month: 'long', day: 'numeric' })}`);
    doc.moveDown();

    // Medications table
    doc.fontSize(13).fillColor('#0d9488').text('Medications');
    doc.moveDown(0.5);
    (meds || []).forEach((m, i) => {
      doc.fontSize(11).fillColor('#1e293b').text(`${i + 1}. ${m.drug_name || '--'}`, { continued: false });
      doc.fontSize(10).fillColor('#64748b');
      if (m.dosage) doc.text(`   Dosage: ${m.dosage}`);
      if (m.frequency) doc.text(`   Frequency: ${m.frequency}`);
      if (m.duration) doc.text(`   Duration: ${m.duration}`);
      doc.moveDown(0.5);
    });

    // Notes
    if (rx.notes) {
      doc.moveDown();
      doc.fontSize(11).fillColor('#0d9488').text('Clinical Notes');
      doc.fontSize(10).fillColor('#334155').text(rx.notes);
    }

    // Footer
    doc.moveDown(2);
    doc.fontSize(8).fillColor('#94a3b8').text(
      'This prescription was generated by MedAssist AI. This is an educational CS 595 project and is NOT a substitute for professional medical advice.',
      { align: 'center' }
    );

    doc.end();
  } catch (err) {
    console.error('Patient prescription PDF error:', err);
    res.status(500).json({ error: 'Failed to generate PDF' });
  }
});


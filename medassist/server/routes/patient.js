// GET /api/patient/sessions/:id/export-pdf — generate PDF for a complete session
router.get('/sessions/:id/export-pdf', verifyToken, auditLog('export_pdf', 'session'), async (req, res) => {
  try {
    const session = await getSessionById(req.params.id, req.user.userId);
    if (!session) {
      return res.status(404).json({ error: 'Session not found' });
    }

    // Fetch linked blood report if any
    let analysis = {};
    let tabletRecommendations = [];
    let riskScores = null;
    let followUp = null;

    if (session.report_id) {
      const { rows } = await pool.query(
        'SELECT analysis, tablet_recommendations, risk_scores, follow_up FROM blood_reports WHERE id = $1',
        [session.report_id]
      );
      if (rows.length) {
        analysis = rows[0].analysis || {};
        tabletRecommendations = rows[0].tablet_recommendations || [];
        riskScores = rows[0].risk_scores || null;
        followUp = rows[0].follow_up || null;
      }
    }

    // Fetch patient name
    const { rows: userRows } = await pool.query('SELECT full_name FROM users WHERE id = $1', [req.user.userId]);

    const pdfBuffer = await generateSessionPDF({
      patientName: userRows[0]?.full_name || 'Patient',
      disease: session.selected_disease || 'N/A',
      symptoms: session.symptoms || [],
      analysis,
      tabletRecommendations,
      riskScores,
      followUp,
    });

    res.setHeader('Content-Type', 'application/pdf');
    res.setHeader('Content-Disposition', `attachment; filename=MedAssist_Report_${req.params.id}.pdf`);
    res.send(pdfBuffer);
  } catch (err) {
    console.error('Export PDF error:', err);
    res.status(500).json({ error: 'Failed to generate PDF' });
  }
});

// POST /api/patient/sessions/:id/share — generate a share token for a session
router.post('/sessions/:id/share', verifyToken, auditLog('share_report', 'session'), async (req, res) => {
  try {
    const session = await getSessionById(req.params.id, req.user.userId);
    if (!session) {
      return res.status(404).json({ error: 'Session not found' });
    }

    const shareToken = crypto.randomBytes(24).toString('hex');
    const expiresAt = new Date(Date.now() + 7 * 24 * 60 * 60 * 1000); // 7 days

    await pool.query(
      `INSERT INTO report_shares (token, session_id, patient_id, expires_at)
       VALUES ($1, $2, $3, $4)`,
      [shareToken, req.params.id, req.user.userId, expiresAt]
    );

    const clientUrl = process.env.CLIENT_URL || 'http://localhost:5173';
    res.json({
      token: shareToken,
      url: `${clientUrl}/shared/${shareToken}`,
      expiresAt,
    });
  } catch (err) {
    console.error('Share session error:', err);
    res.status(500).json({ error: 'Failed to share session' });
  }
});

// GET /api/patient/vitals — get patient's vitals (query: type, days)
router.get('/vitals', verifyToken, async (req, res) => {
  try {
    const { type, days } = req.query;
    let query = 'SELECT * FROM vitals_logs WHERE patient_id = $1';
    const params = [req.user.userId];

    if (type) {
      params.push(type);
      query += ` AND type = $${params.length}`;
    }
    if (days) {
      params.push(parseInt(days));
      query += ` AND recorded_at >= NOW() - INTERVAL '1 day' * $${params.length}`;
    }

    query += ' ORDER BY recorded_at DESC LIMIT 200';
    const { rows } = await pool.query(query, params);
    res.json({ vitals: rows });
  } catch (err) {
    console.error('Get vitals error:', err);
    res.status(500).json({ error: 'Failed to fetch vitals' });
  }
});


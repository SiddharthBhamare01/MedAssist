// PUT /api/patient/medical-id — save/update medical ID
router.put('/medical-id', verifyToken, async (req, res) => {
  const { emergency_name, emergency_phone, blood_type, organ_donor, critical_notes, pin } = req.body;

  try {
    let pinHash = null;
    if (pin) {
      pinHash = await bcrypt.hash(String(pin), 10);
    }

    // Check if record exists
    const { rows: existing } = await pool.query(
      'SELECT id FROM medical_id WHERE patient_id = $1',
      [req.user.userId]
    );

    let result;
    if (existing.length > 0) {
      const updateFields = [
        'emergency_name = $2', 'emergency_phone = $3', 'blood_type = $4',
        'organ_donor = $5', 'critical_notes = $6',
      ];
      const params = [req.user.userId, emergency_name || null, emergency_phone || null,
        blood_type || null, organ_donor || false, critical_notes || null];

      if (pinHash) {
        params.push(pinHash);
        updateFields.push(`pin_hash = $${params.length}`);
      }

      result = await pool.query(
        `UPDATE medical_id SET ${updateFields.join(', ')} WHERE patient_id = $1 RETURNING id, patient_id, emergency_name, emergency_phone, blood_type, organ_donor, critical_notes`,
        params
      );
    } else {
      result = await pool.query(
        `INSERT INTO medical_id (patient_id, emergency_name, emergency_phone, blood_type, organ_donor, critical_notes, pin_hash)
         VALUES ($1, $2, $3, $4, $5, $6, $7)
         RETURNING id, patient_id, emergency_name, emergency_phone, blood_type, organ_donor, critical_notes`,
        [req.user.userId, emergency_name || null, emergency_phone || null,
         blood_type || null, organ_donor || false, critical_notes || null, pinHash]
      );
    }

    res.json({ medicalId: result.rows[0] });
  } catch (err) {
    console.error('Save medical ID error:', err);
    res.status(500).json({ error: 'Failed to save medical ID' });
  }
});

// GET /api/patient/health-timeline — aggregate session data into timeline events
router.get('/health-timeline', verifyToken, async (req, res) => {
  try {
    const { rows } = await pool.query(
      `SELECT
         s.id AS session_id,
         s.selected_disease AS disease,
         s.status,
         s.created_at,
         'session' AS event_type,
         s.symptoms
       FROM symptom_sessions s
       WHERE s.patient_id = $1
       ORDER BY s.created_at DESC
       LIMIT 50`,
      [req.user.userId]
    );

    // Also get vitals as events
    const { rows: vitals } = await pool.query(
      `SELECT id, type, value, value2, unit, recorded_at AS created_at, 'vital' AS event_type
       FROM vitals_logs
       WHERE patient_id = $1
       ORDER BY recorded_at DESC
       LIMIT 50`,
      [req.user.userId]
    );

    // Merge and sort by date
    const timeline = [...rows, ...vitals].sort(
      (a, b) => new Date(b.created_at) - new Date(a.created_at)
    );

    res.json({ timeline });
  } catch (err) {
    console.error('Health timeline error:', err);
    res.status(500).json({ error: 'Failed to fetch timeline' });
  }
});

// GET /api/patient/symptoms/trends — compare symptom severity across sessions
router.get('/symptoms/trends', verifyToken, async (req, res) => {
  try {
    const { rows } = await pool.query(
      `SELECT id, symptoms, selected_disease, created_at
       FROM symptom_sessions
       WHERE patient_id = $1
       ORDER BY created_at DESC
       LIMIT 20`,
      [req.user.userId]
    );

    // Build trend data: extract symptom names and severities per session
    const trends = rows.map((s) => {
      const symptoms = Array.isArray(s.symptoms) ? s.symptoms : [];
      return {
        sessionId: s.id,
        disease: s.selected_disease,
        date: s.created_at,
        symptoms: symptoms.map((sym) => ({
          name: typeof sym === 'string' ? sym : sym.symptom || sym.name || 'unknown',
          severity: sym.severity || null,
          duration: sym.duration || null,
        })),
      };
    });

    res.json({ trends });
  } catch (err) {
    console.error('Symptom trends error:', err);
    res.status(500).json({ error: 'Failed to fetch symptom trends' });
  }
});

// POST /api/patient/sessions/:id/second-opinion — re-run diagnostic agent on same session
